import { existsSync, mkdirSync, readdirSync, copyFileSync, writeFileSync, statSync } from 'node:fs'
import { join, extname, basename } from 'node:path'

const SRC_DIR = 'other_certificates'
const DST_DIR = 'public/certificates'
const TS_OUT  = 'src/certificates.generated.ts'

if (!existsSync(SRC_DIR)) {
  console.error(`Folder "${SRC_DIR}" not found at project root. Create it and drop your certificate files there.`)
  process.exit(1)
}

if (!existsSync(DST_DIR)) mkdirSync(DST_DIR, { recursive: true })

const ALLOWED = new Set(['.png', '.jpg', '.jpeg', '.webp', '.gif', '.pdf'])

function prettyTitle(filename) {
  const base = basename(filename, extname(filename))
  return base
    .replace(/[_\-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/\b\w/g, (c) => c.toUpperCase())
}

const items = []
for (const entry of readdirSync(SRC_DIR)) {
  const srcPath = join(SRC_DIR, entry)
  if (!statSync(srcPath).isFile()) continue
  const ext = extname(entry).toLowerCase()
  if (!ALLOWED.has(ext)) continue

  const safeName = entry.replace(/\s+/g, '_')
  const dstPath = join(DST_DIR, safeName)
  copyFileSync(srcPath, dstPath)

  items.push({
    title: prettyTitle(entry),
    file: `/certificates/${safeName}`,
    type: ext === '.pdf' ? 'pdf' : 'image',
  })

  console.log(`copied  ${entry}  ->  ${dstPath}`)
}

items.sort((a, b) => a.title.localeCompare(b.title))

const tsBody = `// AUTO-GENERATED by prepare-certificates.mjs — do not edit by hand.
export type OtherCertificate = { title: string; file: string; type: 'image' | 'pdf' }

export const otherCertificates: OtherCertificate[] = ${JSON.stringify(items, null, 2)}
`

writeFileSync(TS_OUT, tsBody)
console.log(`\nWrote ${items.length} certificates to ${TS_OUT}`)
